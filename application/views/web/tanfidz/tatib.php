        <div class="container">
            <header>
                <p id="bab1"><b>BAB I</b></p>
                <p><b>KETENTUAN UMUM</b></p>
                <p>Pasal 1</p>
                <p>Pimpinan dalam tata tertib ini adalah Pimpinan Komisariat, Pimpinan Cabang, Dewan Pimpinan Daerah dan Dewan Pimpinan Pusat Ikatan Mahasiswa Muhammadiyah.</p>
                <p>Pasal 2</p>
                <p>Tanggung jawab pemilihan terletak pada pimpinan masing-masing tingkat.</p>
                <p>Pasal 3</p>
                <p>Pemilihan pimpinan dapat dilakukan dalam:</p>
                <p>1. Muktamar untuk memilih Dewan Pimpinan Pusat.</p>
                <p>2. Musyawarah Daerah untuk memilih Dewan Pimpinan Daerah.</p>
                <p>3. Musyawarah Cabang untuk memilih Pimpinan Cabang.</p>
                <p>4. Musyawarah Komisariat untuk memilih Pimpinan Komisariat.</p>
                <br>
                <p id="bab2"><b>BAB II</b></p>
                <p><b>PANITIA PEMILIHAN</b></p>
                <p>Pasal 4</p>
                <p>Panitia Pemilihan yang selanjutnya disebut Panlih adalah panitia yang bertugas secara penuh untuk melakukan proses pemilihan formatur.</p>
                <p>Pasal 5</p>
                <p>Panlih dipilih dan ditetapkan oleh pimpinan masing-masing tingkatan melalui rapat pleno pimpinan.</p>
                <p>Pasal 6</p>
                <p>Anggota panlih dapat dipilih dari pimpinan dan anggota, sekurang-kurangnya 3 ( tiga) orang dan sebanyak-banyaknya 5 (lima) orang.</p>
                <p>Pasal 7</p>
                <p>Setiap kader ikatan yang menjadi panlih tidak dapat dicalonkan sebagai Formatur.</p>
                <p>Pasal 8</p>
                <p>Tugas Panitia Pemilihan adalah :</p>
                <p>1. Menyampaikan formulir pencalonan formatur kepada pimpinan masing-masing  tingkat  di  bawahnya,  kecuali  pimpinan  komisariat  kepada anggotanya.</p>
                <p>2. Menerima usulan calon formatur.</p>
                <p>3. Meneliti  dan  menyeleksi  persyaratan  administrasi  dan  syarat calon sesuai dengan peraturan yang berlaku.</p>
                <p>4. Mengumumkan nama-nama calon formatur.</p>
                <p>5. Memimpin pelaksanaan pemilihan sampai terpilih formatur.</p>
                <p>6. Menetapkan dan mengumumkan seluruh hasil pemilihan kepada peserta musyawarah sebelum permusyawaratan ditutup. </p>
                <p>7. Menyerahkan hasil pemilihan kepada Formatur terpilih.</p>
                <p>Pasal 9</p>
                <p>Tugas  panlih  dinyatakan  selesai setelah menyerahkan hasil berkas pemilihan kepada formatur terpilih.</p>
                <br>
                <p id="bab3"><b>BAB III</b></p>
                <p><b>SYARAT-SYARAT FORMATUR</b></p>
                <p>Pasal 10</p>
                <p>Syarat Umum Formatur Syarat-syarat untuk dapat dicalonkan menjadi formatur adalah:</p>
                <p>1. Setia kepada asas, tujuan, perjuangan Ikatan dan persyarikatan.</p>
                <p>2. Taat kepada garis kebijakan pimpinan Ikatan dan persyarikatan.</p>
                <p>3. Mampu membaca Al-qur'an secara tartil.</p>
                <p>4. Dapat menjadi teladan utama dalam organisasi, terutama akhlak dan ibadah.</p>
                <p>5. Berpengalaman menjadi pimpinan Ikatan setingkat di bawahnya, kecuali Pimpinan Komisariat.</p>
                <p>6. Mendapat rekomendasi dari Pimpinan IMM level pimpinan di bawahnya.</p>
                <p>7. Bersedia berdomisili di tempat kedudukan sekretariat, jika terpilih menjadi pimpinan.</p>
                <p>8. Tidak merangkap dengan pimpinan partai politik dan organisasi mahasiswa ekstra lainnya.</p>
                <p>9. Membuat surat kesediaan untuk tidak dicalonkan atau mencalonkan diri menjadi calon legislatif.</p>
                <p>Pasal 11</p>
                <p>Syarat Khusus Formatur</p>
                <p>1. Syarat khusus Dewan Pimpinan Pusat :</p>
                <p>a. Telah menjadi anggota biasa sekurang-kurangnya 3 (tiga) tahun.</p>
                <p>b. Telah lulus Darul Arqam Paripurna.</p>
                <p>c. Usia maksimal 30 tahun.</p>
                <p>d. Pendidikan minimal pascasarjana atau sedang menempuh pascasarjana.</p>
                <p>2. Syarat Khusus Dewan Pimpinan Daerah:</p>
                <p>a. Telah menjadi anggota biasa sekurang-kurangnya 3 (tiga) tahun.
                <p>b. Telah lulus Darul Arqam Madya.</p>
                <p>c. Usia maksimal 28 tahun.</p>
                <p>3. Syarat Khusus Pimpinan Cabang:</p>
                <p>a. Telah menjadi anggota biasa sekurang-kurangnya 2 (dua) tahun.</p>
                <p>b. Telah lulus Darul Arqam Madya.</p>
                <p>c. Usia maksimal 26 tahun.</p>
                <p>4. Syarat khusus Pimpinan Komisariat:</p>
                <p>a. Telah menjadi anggota biasa sekurang-kurangnya 1 (satu) tahun.</p>
                <p>b. Telah lulus perkaderan Darul Arqam Dasar.</p>
                <br>
                <p id="bab4"><b>BAB IV</b></p>
                <p><b>PENCALONAN</b></p>
                <p>Pasal 12</p>
                <p>1. Pencalonan Formatur Dewan Pimpinan Pusat:</p>
                <p>a. Setiap Dewan Pimpinan Daerah secara kelembagaan berhak mengajukan calon anggota formatur sebanyak 13 (tiga belas) orang, yang disampaikan secara tertulis kepada panitia pemilihan pusat.</p>
                <p>b. Dewan Pimpinan Pusat secara kelembagaan berhak mengajukan calon anggota formatur sebanyak 13 (tiga belas) orang, yang disampaikan secara tertulis kepada panitia pemilihan pusat.</p>
                <p>c. Pengajuan nama-nama calon anggota formatur tersebut disampaikan secara tertulis kepada panitia pemilihan pusat selambat-lambatnya 1 (satu) bulan sebelum diselenggarakan Muktamar.</p>
                <p>d. Jika calon anggota formatur yang memenuhi syarat kurang dari 13 (tiga belas) orang, maka batas waktu pengesahan calon anggota formatur diperpanjang hingga menjelang proses pemilihan.</p>
                <p>2. Pencalonan Formatur Dewan Pimpinan Daerah:</p>
                <p>a. Setiap Pimpinan Cabang secara kelembagaan berhak mengajukan calon sebanyak 13  (tiga belas) orang, yang disampaikan secara tertulis kepada panitia pemilihan daerah.</p>
                <p>b. Dewan Pimpinan Daerah secara kelembagaan berhak mengajukan calon sebanyak 13  (tga belas) orang, yang disampaikan secara tertulis kepada panitia pemilihan daerah.</p>
                <p>c. Pengajuan nama-nama calon tersebut disampaikan secara tertulis kepada panitia pemilihan daerah selambat-lambatnya 3 (tiga) minggu sebelum diselenggarakan Musyawarah Daerah.</p>
                <p>d. Jika calon anggota formatur yang memenuhi syarat kurang dari 13 (tiga belas) orang, maka batas waktu pengesahan calon anggota formatur diperpanjang hingga menjelang proses pemilihan.</p>
                <p>3. Pencalonan Formatur Pimpinan Cabang:</p>
                <p>a. Setiap Pimpinan Komisariat secara kelembagaan berhak mengajukan calon sebanyak 13 (tiga belas) orang, yang disampaikan secara tertulis kepada panitia pemilihan cabang.</p>
                <p>b. Pimpinan Cabang secara kelembagaan berhak mengajukan calon sebanyak 13(tiga belas) orang, yang disampaikan secara tertulis kepada panitia pemilihan cabang.</p>
                <p>c. Pengajuan nama-nama calon tersebut disampaikan secara tertulis kepada panitia pemilihan cabang selambat-lambatnya 2 (dua) minggu sebelum diselenggarakan Musyawarah Cabang.</p>
                <p>d. Jika calon anggota formatur yang memenuhi syarat kurang dari 13 (tiga belas) orang, maka batas waktu pengesahan calon anggota formatur diperpanjang hingga menjelang proses pemilihan.</p>
                <p>4. Pencalonan Formatur Pimpinan Komisariat:</p>
                <p>a. Setiap anggota berhak mengajukan calon sebanyak 13 (tiga belas) orang, yang disampaikan secara tertulis kepada panitia pemilihan komisariat.</p>
                <p>b. Pimpinan Komisariat secara kelembagaan berhak mengajukan calon sebanyak 13 ( tiga belas) orang, yang disampaiakan secara tertulis kepada panitia pemilihan Komisariat.</p>
                <p>c. Pengajuan nama-nama calon tersebut disampaikan secara tertulis kepada panitia pemilihan komisariat selambat-lambatnya 1 (satu) minggu sebelum diselenggarakan Musyawarah Komisariat.</p>
                <p>d. Jika calon anggota formatur yang memenuhi syarat kurang dari 13 (tiga belas) orang, maka batas waktu pengesahan calon anggota formatur diperpanjang hingga menjelang proses pemilihan.</p>
                <br>
                <p id="bab5"><b>BAB V</b></p>
                <p><b>PELAKSANAAN PEMILIHAN</b></p>
                <p>Pasal 13</p>
                <p>Pemilihan dilakukan secara langsung, bebas, rahasia, jujur dan adil.</p>
                <br>
                <p id="bab6"><b>BAB VI</b></p>
                <p><b>TATA  CARA PEMILIHAN</b></p>
                <p>Pasal 14</p>
                <p>Pemilihan Formatur</p>
                <p>1. Setiap peserta berhak memilih 13 (tiga belas) nama calon formatur dan jika peserta memilih nama calon formatur kurang atau lebih dari 13 (tiga belas) maka suara dinyatakan tidak sah.</p>
                <p>2. Calon anggota formatur yang mendapat suara terbanyak 1 sampai 13, dinyatakan sah sebagai anggota formatur terpilih.</p>
                <p>3. Apabila terdapat suara berimbang pada suara terbanyak terakhir, maka diadakan pemilihan ulang sampai terdapat selisih suara.</p>
                <p>Pasal 15</p>
                <p>Tugas  Formatur Terpilih</p>
                <p>1. Formatur terpilih menyenggarakan sidang formatur untuk:</p>
                <p>a. Memilih Ketua Umum</p>
                <p>b. Menyusun komposisi kepengurusan.</p>
                <p>c. Menyusun personalia pimpinan secara lengkap dan sudah harus terbentuk selambat-lambatnya sebelum acara penutupan musyawarah.</p>
                <p>2. Apabila poin 3 tidak terpenuhi, maka penentuan personalia pimpinan secara lengkap paling lambat 15 hari setelah selesai musyawarah untuk Dewan Pimpinan Pusat, 7 hari untuk Dewan Pimpinan Daerah, 7 hari untuk Pimpinan Cabang dan 7  hari Pimpinan Komisariat.</p>
                <p>3. Komposisi kepengurusan sedapat mungkin ditetapkan dari formatur terpilih dan calon formatur.</p>
                <p>Pasal 16</p>
                <p>Hasil sidang Formatur diumumkan pada saat penutupan musyawarah.</p>
                <p>Pasal 17</p>
                <p>Segala hal yang belum diatur dalam tata tertib pemilihan ini akan ditetapkan kemudian selama tidak bertentangan dengan AD/ART IMM.</p>
                <p>Pasal 18</p>
                <p>Tata tertib pemilihan ini berlaku sejak tanggal ditetapkan dan menjadi pengganti tata tertib pemilihan sebelumnya.</p>
