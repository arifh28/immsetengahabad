            <p><b>TATA HUBUNGAN</b></p>
            <p>Pasal 9</p>
            <p>1. Ketua Umum adalah pimpinan tertinggi semua unit kerja, apabila Ketua Umum berhalangan, maka jabatan ditugaskan kepada anggota Pimpinan IMM yang sebagai pejabat berdasarkan musyawarah Pimpinan IMM.</p>
            <p>2. Sekretaris Umum adalah pengendali atas segala bahan informasi masuk maupun keluar Pimpinan IMM dan oleh karenanya Sekretaris Umum bertanggungjawab atas terselenggaranya kelancaran arus informasi ke semua jurusan.</p>
            <p>3. Sekretaris Umum dibantu oleh Sekretaris Bidang atas terselenggaranya rapat-rapat Pimpinan IMM, termasuk persiapan dan penyelesaian hasil-hasil rapat.</p>
            <p>4. Apabila Sekretaris Umum berhalangan, maka jabatan ditugaskan kepada salah satu Sekretaris sebagai pejabat berdasarkan musyawarah Pimpinan IMM.</p>
            <p>5. Bendahara Umum adalah penanggungjawab pengadaan dan penggunaan dana, yang dalam tugasnya dibantu oleh para wakil bendahara.</p>
            <p>6. Apabila Bendahara Umum berhalangan, maka jabatan ditugaskan kepada salah seorang Wakil Bendahara sebagai pejabat berdasarkan musyawarah Pimpinan IMM.</p>
            <p>7. Ketua-ketua Bidang bertanggungjawab atas pelaksanaan sektor�sektor kegiatan pada bidang bersangkutan, khususnya dalam melaksanakan keputusan Muktamar/Tanwir serta program yang telah digariskan.</p>
            <p>8. Ketua bidang adalah pengarah, koordinator dan pengendali pelaksanaan kegiatan bidang yang dilimpahkan unit-unit kegiatan Pimpinan IMM dan atau level pimpinan di bawahnya.</p>
            <p>9. Sekretaris Bidang bertanggungjawab bersama Ketua Bidang dalam pelaksanaan tugas Bidangnya dan sekaligus menjadi Sekretaris Bidang.</p>
            <p>10. Apabila Ketua dan Sekretaris Bidang berhalangan, maka jabatan ditugaskan kepada anggota Pimpinan IMM lainnya berdasarkan musyawarah.</p>
            <p>11. Hubungan kerja horizontal antar Badan Pimpinan Otonom dan atau dengan pihak lain harus dilakukan sepengetahuan Pimpinan IMM.</p>
            <p>12. Dalam melaksanakan tugasnya, Badan Pimpinan Otonom melakukan koordinasi dengan Ketua Bidang terkait dan dikontrol oleh Ketua Umum sebagai pimpinan tertinggi.</p>
            <p>13. Lalu Lintas keuangan Badan Pimpinan Otonom, dikoordinir oleh Wakil Bendahara II untuk keperluan laporan keuangan Ikatan secara menyeluruh.</p>
            <p>14. Badan Pimpinan Otonom (BPO) bertanggungjawab kepada Ketua Umum.</p>
            <p>15. Personil Unit Pelaksana Teknis berasal dari lintas lembaga-lembaga yang dibentuk untuk melaksanakan tugas-tugas khusus dan atau berkala.</p>
            <p>16. Setiap akhir pelaksanaan tugasnya, Unit Pelaksana Teknis membuat laporan kegiatan untuk disampaikan kepada Pimpinan IMM, selanjutnya menjadi bahan laporan Pimpinan IMM setiap Tanwir/Muktamar, Musyda, Musycab, atau Musykom.</p>
            <br>
            <p id="bab8"><b>BAB VIII</b></p>
            <p><b>LEMBAGA MUSYAWARAH</b></p>
            <p>Pasal 10</p>
            <p>Yang dimaksud dengan Lembaga Musyawarah adalah rapat-rapat Pimpinan IMM terdiri dari :</p>
            <p>1. Rapat Pleno</p>
            <p>2. Rapat Badan Pimpinan Harian</p>
            <p>3. Rapat Koordinasi Bidang</p>
            <p>4. Rapat Badan Pimpinan Otonom</p>
            <p>5. Rapat Kerja Gabungan.</p>
            <p>6. Rapat-rapat tersebut dinyatakan sah tanpa memandang jumlah yang hadir selama undangan secara sah sudah disampaikan.</p>
            <p>Pasal 9</p>
            <p>Rapat Pleno</p>
            <p>1. Rapat Pleno adalah rapat yang diikuti oleh anggota Pimpinan IMM, Ketua dan Sekretaris Badan Pimpinan Otonom.</p>
            <p>2. Rapat Pleno diadakan sekurang-kurangnya tiga bulan sekali yang waktunya disepakati oleh Rapat BPH sebelumnya, dan atau dalam keadaan tertentu Rapat Pleno diadakan untuk itu.</p>
            <p>3. Rapat Pleno berfungsi sebagai forum pembahasan dan musyawarah pengambilan keputusan Pimpinan IMM dalam menjalankan fungsi dan tugasnya, khususnya yang berkenaan dengan:</p>
            <p>a. Pentanfidzan Keputusan Musyawarah dan Rapat Kerja.</p>
            <p>b. Perencanaan dan atau kebijakan Bidang.</p>
            <p>c. Laporan-laporan Bidang.</p>
            <p>d. Laporan-laporan lembaga Otonom.</p>
            <p>e. Pemecahan masalah mendasar organisasi dan pimpinan.</p>
            <p>f. Penentuan keputusan lainnya dan sikap Ikatan yang berdampak luas pada masyarakat, umat dan bangsa.</p>
            <p>4. Agenda Rapat Pleno ditetapkan berdasarkan kepentingan.</p>
            <p>5. Apabila ada permasalahan penting, mendesak dan berskala nasional, maka Rapat Pleno dapat menghadirkan ketua Umum.</p>
            <p>6. Rapat Pleno dipimpin oleh Ketua Umum dan atau Pimpinan yang ditugaskan.</p>
            <p>7. Semua keputusan Rapat Pleno hanya bisa dibatalkan oleh Rapat Pleno berikutnya.</p>
            <p>Pasal 10</p>
            <p>Rapat Badan Pimpinan Harian (BPH)</p>
            <p>1. Rapat Badan Pimpinan Harian adalah rapat yang diikuti oleh anggota Pimpinan IMM, diutamakan anggota Badan Pimpinan Harian, diadakan secara reguler sekurangkurangnya 1 (satu) minggu sekali yang waktunya ditetapkan oleh Ketua Umum/ Sekretaris Umum.</p>
            <p>2. Rapat Badan Pimpinan Harian adalah bagian subordinatif dan memperoleh pelimpahan wewenang dari rapat pleno, dengan tugas:</p>
            <p>a. Penyelesaian masalah pada tingkat  operasional.</p>
            <p>b. Penetapan rencana kebijakan taktis operasional, khususnya yang berkaitan dengan pengelolaan Lembaga Otonom, termasuk hubungan eksternal.</p>
            <p>c. Pembahasan dan pengelolaan masalah yang dijadikan bahan atau usulan untuk Rapat Pleno.</p>
            <p>3. Agenda rapat BPH terdiri dari:</p>
            <p>a. Siraman rohani</p>
            <p>b. Informasi aktual</p>
            <p>c. Evaluasi aktivitas Pimpinan pasca rapat  BPH sebelumnya.</p>
            <p>d. Pembahasan kebijakan/program.</p>
            <p>e. Pembahasan surat masuk.</p>
            <p>f. Agenda lain.</p>
            <p>4. Rapat BPH dipimpin oleh anggota BPH yang ditugaskan.</p>
            <p>Pasal 11</p>
            <p>Rapat Koordinasi Bidang</p>
            <p>1. Rapat Koordinasi Bidang adalah rapat yang diikuti Ketua dan Sekretaris Bidang dan anggota Badan Pimpinan Otonom terkait. Mengingat status Badan Pimpinan Otonom adalah lembaga Otonom, maka rapat inipun sifatnya koordinatif, bukan instruktif.</p>
            <p>2. Rapat Koordinasi Bidang ini sewaktu-waktu dapat diikuti oleh Pimpinan IMM di bawahnya untuk pelaksanaan Program Bidang yang sifatnya koordinatif.</p>
            <p>3. Rapat Koordinasi bertugas untuk membahas :</p>
            <p>a. Kebijakan dan perencanaan operasional serta strategi implementasi program.</p>
            <p>b. Memecahkan masalah lingkup bidang dan menerima aspirasi lingkungan luar yang relevan.</p>
            <p>c. Menyusun usulan perencanaan strategis lingkup bidang yang dianggap perlu untuk diputuskan ditingkat rapat BPH.</p>
            <p>4. Agenda Rapat Koordinasi Bidang ditetapkan oleh ketua/sekretaris Bidang atas amanah  Rapat BPH dan atau diusulkan oleh anggota Badan Pimpinan Otonom.</p>
            <p>5. Rapat Koordinasi Bidang dipimpin oleh Ketua Umum dan atau Ketua Bidang yang ditugaskan.</p>
            <p>Pasal 12</p>
            <p>Rapat Badan Pimpinan Otonom</p>
            <p>1. Rapat Badan Pimpinan Otonom diikuti oleh anggota Badan Pimpinan Otonom yang diadakan secara reguler, sekurang-kurangnya dua minggu sekali atau sesuai kebutuhan.</p>
            <p>2. Mengingat prinsip kerja DPO/LO ini adalah proporsional dan profesional, maka rapat-rapatnya juga dapat dibagi lagi ke dalam rapat tim kerja, panitia pelaksana dan sebagainya menurut keperluan tuntunan manajemen, namun tidak terpisahkan sebagai satu kesatuan rapat Badan Pimpinan Otonom.</p>
            <p>3. Rapat Badan Pimpinan Otonom bertugas untuk membahas kebijakan-kebijakan operasional program mandiri maupun mitra yang meliputi :</p>
            <p>a. Pengorganisasian  sampai pengendalian teknis operasional, meliputi perencanaan pelaksanaan program kerja.</p>
            <p>b. Perencanaan/pencarian mitra kerja dan penggalian sumber dana.</p>
            <p>c. Evaluasi kerja mingguan.</p>
            <p>d. Membahas persoalan yang timbul dalam lingkungan BPO.</p>
            <p>4. Rapat BPO dipimpin oleh ketua BPO dan atau yang ditugaskan.</p>
            <p>Pasal 13</p>
            <p>Rapat Kerja Gabungan</p>
            <p>1. Rapat Kerja Gabungan adalah rapat Pimpinan IMM yang menghimpun seluruh unit kerja Pimpinan IMM, diadakan setahun sekali dan atau sesuai kebutuhan.</p>
            <p>2. Rapat Kerja Gabungan bertugas untuk membahas :</p>
            <p>a. Menetapkan Visi-Misi Organisasi dan atau mereview Visi-Misi sebelumnya.</p>
            <p>b. Uraian program kerja amanat Muktamar dan atau mereview Hasil Rapat Kerja Gabungan sebelumnya.</p>
            <p>3. Dalam keadaan tertentu RKG mengeluarkan Rekomendasi.</p>
            <p>4. Agenda Rapat Kerja Gabungan ditetapkan oleh Pimpinan IMM.</p>
            <p>5. Rapat Kerja Gabungan dipimpin oleh Ketua Umum atau yang ditugaskan.</p>
            <br>
            <p id="bab9"><b>BAB IX</b></p>
            <p><b>PERATURAN TAMBAHAN</b></p>
            <p>Pasal 14</p>
            <p>1. Berdasarkan kebutuhan, maka DPP IMM dapat membentuk Badan Pimpinan Semi Otonom (BPSO) seperti lembaga, Biro, atau korps dan semacamnya yang berstatus Semi Otonom.</p>
            <p>2. Semua peraturan tambahan yang timbul kehendak poin 1 pasal 14 tersebut, maka diatur dalam kaidah tersendiri.</p>
            <p>3. Mekanisme kerja ini dapat diterapkan dan dijadikan pedoman untuk mekanisme kerja di semua tingkatan (DPP IMM, DPD IMM, PC IMM dan Pimpinan Komisariat IMM se-Indonesia).</p>
