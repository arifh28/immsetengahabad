            <p>PIMPINAN</p>
            <p>Pasal 11</p>
            <p>Syarat-syarat Pimpinan.</p>
            <p>Syarat-syarat untuk dapat dicalonkan dan dipilih sebagai Pimpinan Ikatan:</p>
            <p>1. Syarat Umum</p>
            <p>a. Telah menjadi anggota Ikatan Mahasiswa Muhammadiyah.</p>
            <p>b. Setia kepada asas, tujuan dan perjuangan ikatan dan persyarikatan.</p>
            <p>c. Taat kepada garis kebijaksanaan pimpinan ikatan dan pimpinan persyarikatan.</p>
            <p>d. Mampu membaca Al-Qur�an secara tartil.</p>
            <p>e. Mampu dan cakap melaksanakan tugas.</p>
            <p>f. Dapat menjadi tauladan utama dalam organisasi terutama dalam bidang akhlaq dan beribadahnya.</p>
            <p>g. Tidak merangkap dengan pimpinan organisasi politik dan anggota organisasi politik.</p>
            <p>h. Berpengalaman dalam memimpin ikatan setingkat di bawahnya.</p>
            <p>i. Bersedia berdomisili di kota, dimana sekretariat berkedudukan jika terpilih menjadi pimpinan.</p>
            <p>2. Syarat-syarat khusus bagi Dewan Pimpinan Pusat</p>
            <p>a. Telah menjadi anggota biasa sekurang-kurangnya 5 (lima) tahun.</p>
            <p>b. Telah lulus perkaderan Darul Arqam Paripurna.</p>
            <p>c. Batas usia maksimal 30 tahun.</p>
            <p>3. Syarat-syarat khusus bagi Dewan Pimpinan Daerah.</p>
            <p>a. Telah menjadi anggota biasa sekurang-kurangnya 3 (tiga) tahun.</p>
            <p>b. Telah lulus perkaderan Darul Arqam Madya.</p>
            <p>c. Batas usia maksimal 28 Tahun.</p>
            <p>4. Syarat-syarat khusus bagi Pimpinan Cabang.</p>
            <p>a. Telah menjadi anggota biasa sekurang-kurangnya 2 (dua) tahun.</p>
            <p>b. Telah lulus perkaderan Darul Arqam Madya.</p>
            <p>c. Batas usia maksimal 26 Tahun</p>
            <p>5. Syarat-syarat khusus bagi Pimpinan Komisariat.</p>
            <p>a. Telah menjadi anggota biasa sekurang-kurangnya 6 (enam) bulan.</p>
            <p>b. Telah lulus perkaderan Darul Arqom Dasar.</p>
            <p>c. Terdaftar sebagai mahasiswa di Perguruan Tinggi.</p>
