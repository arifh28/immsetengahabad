            <p id="bab5"><b>BAB V</b></p>
            <p>PERMUSYAWARATAN</p>
            <p>Pasal 20</p>
            <p>Muktamar</p>
            <p>1. Muktamar dilaksanakan oleh dan atas tanggung jawab Dewan Pimpinan Pusat.</p>
            <p>2. Muktamar dihadiri oleh :</p>
            <p>a. Peserta</p>
            <p>1) Badan Pimpinan Harian Dewan Pimpinan Pusat.</p>
            <p>2) Wakil Dewan Pimpinan Daerah masing-masing 4 (empat) orang.</p>
            <p>3) Wakil Pimpinan Cabang masing-masing 2 (dua) orang.</p>
            <p>b. Peninjau</p>
            <p>1) Pimpinan Pusat Muhammadiyah, Pimpinan Organisasi Otonom</p>
            <p>2) Muhammadiyah Tingkat Pusat, masing-masing 2 (dua) orang. 2) Mereka yang diundang oleh Dewan Pimpinan Pusat.</p>
            <p>3. Peserta Muktamar berhak menyatakan pendapat, memilih dan dipilih serta memiliki hak 1 (satu) suara, peninjau Muktamar berhak menyatakan pendapat.</p>
            <p>4. Ketentuan tentang waktu dan tempat pelaksanaan serta agenda Muktamar ditetapkan oleh Tanwir.</p>
            <p>5. Acara Pokok Muktamar :</p>
            <p>a. Laporan Dewan Pimpinan Pusat tentang :</p>
            <p>1) Kebijakan Dewan Pimpinan Pusat.</p>
            <p>2) Organisasi.</p>
            <p>3) Keuangan.</p>
            <p>4) Pelaksanaan keputusan Muktamar/Tanwir.</p>
            <p>b. Penyusunan Anggaran Dasar dan Anggaran Rumah Tangga.</p>
            <p>c. Penyusunan Garis-garis Besar Haluan Organisasi, Garis-garis Besar Haluan Kerja, dan Program Kerja.</p>
            <p>d. Pemilihan Formatur Dewan Pimpinan Pusat.</p>
            <p>e. Musyawarah Formatur untuk menentukan Ketua Umum dan menyusun Pimpinan.</p>
            <p>f. Masalah-masalah umum IMM yang bersifat urgen.</p>
            <p>g. Rekomendasi.</p>
            <p>6. Ketentuan tentang tata tertib Muktamar dibuat oleh Dewan Pimpinan Pusat dan disahkan oleh Muktamar.</p>
            <p>7. Pada waktu berlangsungnya Muktamar dapat diselenggarakan acara atau kegiatan pendukung yang tidak mengganggu jalannya Muktamar.</p>
            <p>8. Selambat-lambatnya sebulan setelah muktamar, Dewan Pimpinan Pusat harus menyampaikan hasil keputusan tentang acara pokok Muktamar kepada Pimpinan Pusat Muhammadiyah untuk mendapat pengesahan.</p>
            <p>9. Apabila sampai satu bulan sesudah penyerahan hasil keputusan Muktamar belum ada jawaban dari Pimpinan Pusat Muhammadiyah, maka keputusan dianggap sah.</p>
            <p>10. Selambat-lambatnya dua bulan setelah Muktamar, keputusan Muktamar harus ditanfidzkan oleh Dewan Pimpinan Pusat dan selanjutnya disosialisasikan ke Dewan Pimpinan Daerah se-Indonesia.</p>
            <p>11. Keputusan Muktamar tetap berlaku sampai diubah atau dibatalkan oleh Muktamar berikutnya.</p>
            <p>Pasal 21</p>
            <p>Tanwir</p>
            <p>1. Tanwir dilaksanakan oleh dan atas tanggung jawab Dewan Pimpinan Pusat.</p>
            <p>2. Tanwir dihadiri oleh :</p>
            <p>a. Peserta</p>
            <p>1) Badan Pimpinan Harian Dewan Pimpinan Pusat.</p>
            <p>2) Unsur Pembantu Pimpinan Tingkat Pusat yang jumlahnya ditentukan oleh Dewan Pimpinan Pusat.</p>
            <p>3) Wakil Dewan Pimpinan Daerah masing masing 4 (empat) orang.</p>
            <p>b. Peninjau</p>
            <p>Pimpinan Pusat Muhammadiyah, Pimpinan Organisasi Otonom Muhammadiyah Tingkat Pusat, masing-masing 1 (satu) orang.</p>
            <p>3. Acara Pokok Tanwir :</p>
            <p>a. Laporan kebijakan Dewan Pimpinan Pusat dalam memimpin dan melaksanakan keputusan Muktamar.</p>
            <p>b. Masalah-masalah mengenai kepentingan umum organisasi yang tidak dapat ditangguhkan sampai berlangsungnya Muktamar.</p>
            <p>c. Mempersiapkan tempat dan acara yang akan datang.</p>
            <p>4. Ketentuan tentang tata tertib Tanwir dibuat oleh Dewan Pimpinan Pusat dan disahkan oleh Tanwir.</p>
            <p>5. Pada waktu berlangsungnya Tanwir dapat diselenggarakan acara atau kegiatan pendukung yang tidak mengganggu jalannya Tanwir.</p>
            <p>6. Selambat-lambatnya sebulan setelah Tanwir, Dewan Pimpinan Pusat harus menyampaikan hasil Keputusan Tanwir kepada Pimpinan Pusat Muhammadiyah untuk mendapatkan pengesahan.</p>
            <p>7. Apabila sampai satu bulan sesudah penyerahan hasil keputusan Tanwir belum ada jawaban dari Pimpinan Pusat Muhammadiyah, maka keputusan dianggap sah.</p>
            <p>8. Selambat-lambatnya 2 (dua) bulan setelah Tanwir, keputusan Tanwir harus ditanfidzkan oleh Dewan Pimpinan Pusat dan selanjutnya disosialisasi ke Dewan Pimpinan Daerah se-Indonesia.</p>
            <p>9. Keputusan Tanwir mulai berlaku setelah ditanfidzkan oleh Dewan Pimpinan Pusat dan tetap berlaku sampai diubah atau dibatalkan oleh Tanwir atau Muktamar kemudian.</p>
            <p>Pasal 22</p>
            <p>Musyawarah Daerah</p>
            <p>1. Musyawarah Daerah, disingkat Musyda dilaksanakan oleh dan atas tanggung jawab Dewan Pimpinan Daerah.</p>
            <p>2. Musyawarah Daerah dihadiri oleh :</p>
            <p>a. Peserta</p>
            <p>1) Badan Pimpinan Harian Dewan Pimpinan Daerah.</p>
            <p>2) Wakil Pimpinan Cabang masing-masing 4 (empat) orang.</p>
            <p>3) Wakil Pimpinan Komisariat masing-masing 2 (dua) orang. 4) Wakil Dewan Pimpinan Pusat 1 (satu) orang.</p>
            <p>b. Peninjau</p>
            <p>1) Pimpinan Wilayah Muhammadiyah, Pimpinan Organisasi Otonom Muhammadiyah Tingkat Propinsi, masing-masing 2 (dua) orang.</p>
            <p>2) Mereka yang diundang oleh Dewan Pimpinan Daerah.</p>
            <p>3. Peserta Musyda berhak menyatakan pendapat, memilih dan dipilih serta memiliki hak 1 (satu) suara. Peninjau Musyda berhak menyatakan pendapat.</p>
            <p>4. Ketentuan tentang waktu dan tempat pelaksanaan serta agenda Musyda ditetapkan oleh Rapat Pleno diperluas Dewan Pimpinan Daerah.</p>
            <p>5. Acara Pokok Musyawarah Daerah :</p>
            <p>a. Laporan Dewan Pimpinan Daerah tentang :</p>
            <p>1) Kebijakan Dewan Pimpinan Daerah.</p>
            <p>2) Organisasi.</p>
            <p>3) Keuangan.</p>
            <p>4) Pelaksanaan keputusan Muktamar, Tanwir, Musyawarah Daerah serta Instruksi dan ketentuan Dewan Pimpinan Pusat.</p>
            <p>b. Penyusunan program IMM periode berikutnya.</p>
            <p>c. Pemilihan Formatur Dewan Pimpinan Daerah.</p>
            <p>d. Musyawarah Formatur untuk menentukan Ketua Umum dan menyusun Pimpinan.</p>
            <p>e. Masalah-masalah umum IMM yang bersifat urgen dalam daerah.</p>
            <p>f. Rekomendasi.</p>
            <p>6. Ketentuan tentang tata tertib Musyda dibuat oleh Dewan Pimpinan Daerah dan disahkan oleh Musyawarah Daerah.</p>
            <p>7. Pada waktu berlangsungnya Musyda dapat diselenggarakan acara atau kegiatan pendukung yang tidak mengganggu jalannya Musyda.</p>
            <p>8. Selambat-lambatnya sebulan setelah Musyda, Dewan Pimpinan Daerah harus menyampaikan hasil keputusan tentang acara pokok Musyda kepada Dewan Pimpinan Pusat untuk mendapat pengesahan.</p>
            <p>9. Apabila sampai 1 (satu) bulan sesudah penyerahan hasil keputusan Musyda belum ada jawaban dari Dewan Pimpinan Pusat, maka keputusan dianggap sah.</p>
            <p>10. Selambat-lambatnya 2 (dua) bulan setelah Musyda, keputusan Musyda harus ditanfidzkan oleh Dewan Pimpinan Daerah dan selanjutnya disosialisasikan ke Pimpinan Cabang di tempatnya masing-masing.</p>
            <p>11. Keputusan Musyda tetap berlaku sampai diubah atau dibatalkan oleh Musyda berikutnya.</p>
            <p>Pasal 23</p>
            <p>Musyawarah Cabang</p>
            <p>1. Musyawarah Cabang, disingkat Musycab dilaksanakan oleh dan atas tanggung jawab Pimpinan Cabang.</p>
            <p>2. Musyawarah Cabang dihadiri oleh :</p>
            <p>a. Peserta</p>
            <p>1) Badan Pimpinan Harian (BPH)</p>
            <p>2) Wakil Pimpinan Komisariat masing-masing 4 (empat) orang.</p>
            <p>3) Wakil Dewan Pimpinan Daerah 1 (satu) orang.</p>
            <p>b. Peninjau</p>
            <p>1) Pimpinan Daerah Muhammadiyah, Pimpinan Organisasi Otonom Muhammadiyah Tingkat Kota/Kabupaten, masing-masing 2 (dua) orang diundang oleh pimpinan cabang.</p>
            <p>3. Peserta Musycab berhak menyatakan pendapat, memilih dan dipilih serta memiliki hak 1 (satu) suara. Peninjau Musycab berhak menyatakan pendapat.</p>
            <p>4. Ketentuan tentang waktu dan tempat pelaksanaan serta agenda Musycab ditetapkan oleh Rapat Pleno diperluas Pimpinan Cabang.</p>
            <p>5. Acara Pokok Musyawarah Cabang:</p>
            <p>a. Laporan Pimpinan Cabang tentang :</p>
            <p>1) Kebijakan Pimpinan Cabang.</p>
            <p>2) Organisasi.</p>
            <p>3) Keuangan.</p>
            <p>b. Pelaksanaan keputusan Muktamar, Tanwir, Musyawarah Daerah, Musyawarah Cabang serta instruksi dan ketentuan Pimpinan di atasnya.</p>
            <p>c. Penyusunan program IMM periode berikutnya.</p>
            <p>d. Pemilihan Formatur Pimpinan Cabang.</p>
            <p>e. Musyawarah Formatur untuk menentukan Ketua Umum dan menyusun Pimpinan.</p>
            <p>f. Masalah-masalah umum IMM yang bersifat urgen dalam Cabang.</p>
            <p>g. Rekomendasi.</p>
            <p>6. Ketentuan tentang tata tertib Musycab dibuat oleh Pimpinan Cabang dan disahkan oleh Musycab.</p>
            <p>7. Pada waktu berlangsungnya Musycab dapat diselenggarakan acara atau kegiatan pendukung yang tidak mengganggu jalannya Musycab.</p>
            <p>8. Selambat-lambatnya sebulan setelah Musycab, Pimpinan Cabang harus menyampaikan hasil keputusan tentang acara pokok Musycab kepada Dewan Pimpinan Daerah untuk mendapat pengesahan.</p>
            <p>9. Apabila sampai 1 (satu) bulan sesudah penyerahan hasil keputusan Musycab belum ada jawaban dari Dewan Pimpinan Daerah, maka keputusan dianggap sah.</p>
            <p>10. Selambat-lambatnya 2 (dua) bulan setelah Musycab, keputusan Musycab harus ditanfidzkan oleh Pimpinan Cabang dan selanjutnya disosialisasikan ke Pimpinan Komisariat di wilayah masing-masing.</p>
            <p>11. Keputusan Musycab tetap berlaku sampai diubah atau dibatalkan oleh Musycab berikutnya.</p>
            <p>Pasal 24</p>
            <p>Musyawarah Komisariat</p>
            <p>1. Musyawarah Komisariat, disingkat Musykom dilaksanakan oleh dan atas tanggung jawab Pimpinan Komisariat.</p>
            <p>2. Musyawarah Komisariat dihadiri oleh:</p>
            <p>a. Peserta</p>
            <p>1) BPH Pimpinan Komisariat.</p>
            <p>2) Seluruh Anggota Komisariat.</p>
            <p>3) Wakil Pimpinan Cabang 1 (satu) orang.</p>
            <p>b. Peninjau</p>
            <p>1) Mereka yang diundang oleh Pimpinan Komisariat.</p>
            <p>2) Peserta Musykom berhak menyatakan pendapat, memilih dan dipilih serta memiliki hak 1 (satu) suara. Peninjau Musykom berhak menyatakan pendapat.</p>
            <p>3. Ketentuan tentang waktu dan tempat pelaksanaan serta agenda Musykom ditetapkan oleh RapatPleno Pimpinan Komisariat.</p>
            <p>4. Acara Pokok Musyawarah Komisariat:</p>
            <p>a. Laporan Pimpinan Komisariat tentang:</p>
            <p>1) Kebijakan Pimpinan Komisariat</p>
            <p>2) Organisasi</p>
            <p>3) Keuangan</p>
            <p>b. Pelaksanaan keputusan Muktamar, Tanwir, Musyawarah Daerah, Musyawarah Cabang, Musyawarah Komisariat serta Instruksi dan ketentuan Pimpinan di atasnya.</p>
            <p>c. Penyusunan program IMM periode berikutnya.</p>
            <p>d. Pemilihan Formatur Pimpinan Komisariat.</p>
            <p>e. Musyawarah Formatur untuk menentukan Ketua Umum dan menyusun Pimpinan.</p>
            <p>f. Masalah-masalah umum IMM yang bersifat urgen dalam Komisariat.</p>
            <p>g. Rekomendasi.</p>
            <p>5. Ketentuan tentang tata tertib Musykom dibuat oleh Pimpinan Komisariat dan disahkan oleh Musykom.</p>
            <p>6. Pada waktu berlangsungnya Musykom dapat diselenggarakan acara atau kegiatan pendukung yang tidak mengganggu jalannya Musykom.</p>
            <p>7. Selambat-lambatnya 15 (lima belas) hari setelah Musykom, Pimpinan Komisariat harus menyampaikan hasil keputusan tentang acara pokok Musykom kepada Pimpinan Cabang untuk mendapat pengesahan.</p>
            <p>8. Apabila sampai 15 (lima belas) sesudah penyerahan hasil keputusan Musykom belum ada jawaban dari Pimpinan Cabang, maka keputusan dianggap sah.</p>
            <p>9. Selambat-lambatnya 1 (satu) bulan setelah Musykom, keputusan Musykom harus ditanfidzkan oleh Pimpinan Komisariat.</p>
            <p>10. Keputusan Musykom tetap berlaku sampai diubah atau dibatalkan oleh Musykom berikutnya.</p>
            <p>Pasal 25</p>
            <p>Musyawarah Luar Biasa</p>
            <p>1. Musyawarah Luar Biasa, disingkat Musylub dilaksanakan oleh dan atas tanggung jawab pimpinan yang terkait.</p>
            <p>2. Musyawarah Luar Biasa dapat dilaksanakan apabila terjadi kevakuman kepemimpinan selama 6 bulan untuk Dewan Pimpinan Pusat dan Dewan Pimpinan Daerah serta 3 bulan Untuk Pimpinan Cabang dan tidak bisa diselesaikan oleh tim formatur terpilih.</p>
            <p>3. Ketentuan tentang pelaksanaan, tata tertib, susunan acara, dan peserta Musyawarah Luar Biasa, sama dengan ketentuan musyawarah tiap jenjang pimpinan.</p>
            <p>4. Tanggung jawab Musylub bisa dilakuakn oleh jenjang kepemimpinan di atasnya apabila tidak bisa diselesaikan oleh tim formatur terpilih pada musyawarah sebelumnya.</p>
            <p>5. Pelaksanaan Musylub dapat dilakukan apabila diketahui dan disetujui oleh Pimpinan Muhammadiyah setingkat.</p>
            <p>6. Pimpinan akan di karetaker apabila melebihi masa jabatannya selebih-lebihya 4 bulan.</p>
            <p>Pasal 26</p>
            <p>Keputusan Musyawarah</p>
            <p>1. Keputusan Permusyawaratan diusahakan diambil dengan musyawarah untuk mufakat.</p>
            <p>2. Apabila keputusan permusyawaratan terpaksa dilakukan dengan pemungutan suara, maka keputusan diambil dengan suara terbanyak mutlak, yaitu setengah lebih satu dari jumlah peserta yang memberikan hak suara.</p>
            <p>3. Pemungutan suara atas seseorang atau masalah yang penting dapat dilakukan secara tertulis dan rahasia, atau secara langsung.</p>
            <p>4. Apabila dalam pemungutan suara terdapat jumlah suara yang sama banyak, maka pemungutan suara diulangi dengan memberi kesempatan masing-masing pihak untuk menambah penjelasan. Apabila setelah tiga kali pemungutan suara ternyata hasilnya tetap sama atau tidak memenuhi syarat pengambilan keputusan pembicaraan dihentikan tanpa suatu keputusan, atau diserahkan kepada pimpinan di atasnya, sedangkan untuk Muktamar atau Tanwir diserahkan kepada Pimpinan Pusat Muhammadiyah.</p>
            <p>5. Apabila keputusan telah diambil, maka seluruh peserta musyawarah harus menerima keputusan tersebut dengan ikhlas dan tetap bertawakal kepada Allah SWT.</p>
            <br>
            <p id="bab6"><b>BAB VI</b></p>
            <p>LAPORAN</p>
            <p>Pasal 27</p>
            <p>Laporan</p>
            <p>1. Setiap Pimpinan berkewajiban untuk membuat laporan tentang keadaan IMM yang meliputi keorganisasian, gerakan, amal usaha, keuangan dan inventarisasi organisasi, termasuk pula laporan bidang atau lembaga khusus.</p>
            <p>2. Laporan seperti dimaksud pada ayat 1 disampaikan kepada pimpinan di atasnya, dengan ketentuan; bagi Dewan Pimpinan Daerah dan Pimpinan Cabang setiap 6 (enam) bulan, sedangkan bagi Komisariat setiap 3 (tiga) bulan.</p>
            <br>
            <p id="bab7"><b>BAB VII</b></p>
            <p>KEUANGAN</p>
            <p>Pasal 28</p>
            <p>Keuangan</p>
            <p>1. Keperluan IMM secara umum dibiayai bersama oleh Pimpinan Komisariat, Pimpinan Cabang, Dewan Pimpinan Daerah, dan Dewan Pimpinan Pusat.</p>
            <p>2. Keperluan pimpinan IMM setempat dibiayai oleh Pimpinan yang bersangkutan berdasarkan keputusan musyawarah masing-masing.</p>
            <p>3. Uang Pangkal dan Iuran Anggota besarnya ditentukan oleh Dewan Pimpinan Pusat.</p>
            <p>4. Distribusi Uang Pangkal dan Iuran Anggota diatur sebagai berikut:</p>
            <p>a. 50 % untuk Pimpinan Komisariat.</p>
            <p>b. 25 % untuk Pimpinan Cabang.</p>
            <p>c. 15 % untuk Dewan Pimpinan Daerah.</p>
            <p>d. 10 % untuk Dewan Pimpinan Pusat.</p>
            <p>5. Untuk memeriksa keabsahan laporan keuangan dan harta kekayaan, diatur sebagai berikut:</p>
            <p>a. Pemeriksaan dilakukan oleh tim verifikasi yang dibentuk sebelum permusyawaratan.</p>
            <p>b. Tim verifikasi di bentuk dari perwakilan pimpinan dibawahnya atau tim independen.</p>
            <p>c. Ketentuan tentang pemeriksaan diatur dengan peraturan khusus yang ditetapkan oleh Dewan Pimpinan Pusat.</p>
            <p>d. Hasil pemeriksaan dilaporkan dalam permusyawaratan.</p>
            <p>e. Pengelolaan/penarikan keuangan diatur dalam peraturan khusus yang dibuat oleh Dewan Pimpinan Pusat.</p>
            <br>
            <p id="bab8"><b>BAB VIII</b></p>
            <p>PERATURAN KHUSUS DAN PEDOMAN KERJA</p>
            <p>Pasal 29</p>
            <p>Setiap pimpinan dapat membuat peraturan khusus dan pedoman kerja asal tidak bertentangan dengan Anggaran Dasar dan Anggaran Rumah Tangga serta peraturan yang dibuat pimpinan di atasnya.</p>
            <br>
            <p id="bab9"><b>BAB IX</b></p>
            <p>PERUBAHAN ANGGARAN RUMAH TANGGA</p>
            <p>Pasal 30</p>
            <p>Anggaran Rumah Tangga ini dapat diubah oleh Muktamar, dan perubahannya sah apabila disetujui oleh sedikitnya 2/3 (dua pertiga) dari peserta Muktamar yang hadir untuk membicarakan hal tersebut.</p>
            <br>
            <p id="bab10"><b>BAB X</b></p>
            <p>KETENTUAN LAIN</p>
            <p>Pasal 31</p>
            <p>1. Segala ketentuan peraturan yang ada masih tetap berlaku sebelum ada ketentuan atau peraturan baru menurut Anggaran Rumah Tangga ini.</p>
            <p>2. IMM menggunakan tahun takwim, dimulai tanggal 1 Januari dan berakhir 31 Desember.</p>
            <p>3. Pedoman administrasi diatur oleh Dewan Pimpinan Pusat.</p>
            <br>
            <p id="bab11"><b>BAB XI</b></p>
            <p>PENUTUP</p>
            <p>Pasal 32</p>
            <p>1. Segala peraturan yang bertentangan dengan peraturan dalam Anggaran Rumah Tangga ini dinyatakan tidak berlaku lagi.</p>
            <p>2. Hal-hal yang belum diatur dalam Anggaran Rumah Tangga ini diatur lebih lanjut dengan peraturan yang dibuat oleh Dewan Pimpinan Pusat.</p>
            <p>Pasal 33</p>
            <p>Anggaran Rumah Tangga ini menjadi pengganti Anggaran Rumah Tangga sebelumnya, dan telah disahkan oleh Muktamar XVI di Surakarta, Jawa Tengah dan Mulai berlaku sejak disahkannya oleh Pimpinan Pusat Muhammadiyah.</p>

