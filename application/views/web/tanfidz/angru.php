            <header>
            <p id="muqodimah">MUQODIMAH</p>
            <p><i>"Dengan nama Allah yang Maha Pemurah, Maha Penyayang. Segala Puji bagi Allah yang mengasuh semesta alam, yang menguasai hari pembalasan. Hanya kepada engkau kami menyembah dan hanya kepada engkau kami memohon pertolongan. Tunjukanlah kami jalan yang lurus yakni
                    jalan orang-orang yang telah engkau beri kenikmatan atas mereka, bukan jalan orang-orang yang
                    engkau murkai atas mereka dan bukan jalan orang-orang yang sesat.</i></p>
            <p>Bahwa sesungguhnya Islam adalah satu-satunya agama tauhid yang haq di sisi Allah dengan
                berprinsip pada aqidah tauhid dan membawa misi sebagai hudan rahmatan lil'alamin (petunjuk dan
                rahmat bagi sekalian alam). Oleh sebab itu, Islam harus ditegakan dan dilaksanakan dalam
                kehidupan bersama ditengah-tengah masyarakat. Hal tersebut merupakan sunnatullah bagi
                manusia, khususnya umat islam sebagai hamba Allah dan khalifah-Nya di muka bumi ini.
                Persyarikatan Muhammadiyah sebagai gerakan dakwah Islam Amar Ma'ruf Nahi Mungkar
                dan tajdid, adalah salah satu kreasi manusia Muslim dalam upaya menggerakan dan membimbing
                umat agar mampu melaksanakan fungsi dan perannya. Dalam rangka kelangsungan hakikat dan
                misinya, Muhammadiyah memerlukan tumbuhnya kader pelopor, pelangsung dan penyempurna
                cita-cita sekaligus sebagai stabilisator, dinamisator dan gerakan perjuangannya.
                Maka pada 29 Syawal 1384 H. bertepatan dengan tanggal 14 Maret 1964 M. didirikan Ikatan
                Mahasiswa Muhammadiyah (IMM) sebagai salah satu organisasi otonom Muhammadiyah, yang
                merupakan wadah perjuangan untuk menghimpun, menggerakkan dan membina potensi mahasiswa
                Islam guna meningkatkan peran dan tanggung jawabnya sebagai kader persyarikatan, kader umat
                dan kader bangsa, sehingga tumbuh kader-kader yang memiliki kerangka berpikir ilmu amaliyah dan
                kader amal ilmiah sesuai dengan Kepribadian Muhammadiyah, Kesemuanya itu dilaksanakan secara
                bersama dengan menjunjung tinggi musyawarah atas dasar iman dan taqwa serta hanya mengharap
                ridha Allah SWT.</p>
            <p>Dengan dilandasi semangat ketaqwaan kepada Allah SWT, maka penyelenggaraan organisasi
                Ikatan Mahasiswa Muhammadiyah (IMM) berpedoman kepada Anggaran Dasar sebagai berikut:</p>
            <br>
            <p id="bab1"><b>BAB I</b></p>
            <p><b>NAMA, WAKTU, DAN KEDUDUAN</b></p>

            <p>Pasal 1</p>
            <p>Organisasi ini bernama Ikatan Mahasiswa Muhammadiyah
                disingkat IMM adalah gerakan Mahasiswa
                Islam yang beraqidah Islam bersumber Al-Qur'an dan As-Sunah.</p>

            <p>Pasal 2</p>
            <p>IMM didirikan pada tanggal 29 Syawal 1384 H bertepatan
                dengan tanggal 14 Maret 1964 M di
                Yogyakarta untuk waktu yang tidak terbatas.</p>

            <p>Pasal 3</p>
            <p>Tempat kedudukan IMM adalah di tempat kedudukan Dewan
                Pimpinan Pusatnya. Tempat
                kedudukan Dewan Pimpinan Pusatnya seperti tersebut pada ayat 1 adalah di
                Ibukota Negara
                Republik Indonesia.</p>

            <p id="bab2"><b>BAB II</b></p>
            <p><b>ASAS, GERAKAN, DAN LAMBANG</b></p>

            <p>Pasal 4</p>
            <p>Ikatan Mahasiswa Muhammadiyah berazas Islam.</p>

            <p>Pasal 5</p>
            <p>IMM adalah gerakan Mahasiswa Islam yang bergerak di bidang
                keagamaan, kemasyarakatan dan
                kemahasiswaan.</p>

            <p>Pasal 6</p>
            <p>Lambang Ikatan Mahasiswa Muhammadiyah adalah pena yang
                berlapis dengan tiga warna, ditengah
                tertuliskan IMM, bunga melati dan pita yang tercantum tulisan arab fastabiqul khairat serta
                matahari bersinar.</p>
            <img src="file:///android_asset/logo_imm.png" alt="" align="center"/>
            <a href="http://bit.ly/pusatunduhimm">Download Logo</a>

            <p id="bab3"><b>BAB III</b></p>
            <p><b>TUJUAN DAN USAHA</b></p>

            <p>Pasal 7</p>
            <p>Tujuan IMM adalah mengusahakan terbentuknya akademisi Islam
                yang berakhlak mulia dalam
                rangka mencapai tujuan Muhammadiyah.</p>

            <p>Pasal 8</p>
            <p>1. Membina para anggotanya menjadi kader persyarikatan
                Muhammadiyah, kader umat dan kader
                bangsa yang senantiasa setia terhadap keyakinan dan cita-citanya.</p>
            <p>2. Membina para anggotanya untuk selalu tertib dalam
                ibadah, tekun dalam studi dan
                mengamalkan ilmu pengetahuannya untuk melaksanakan ketaqwaan dan pengabdiannya
                kepada Allah SWT.</p>
            <p>3. Membantu para anggota khususnya dan mahasiswa pada
                umumnya dalam menyelesaikan
                kepentingannya.</p>
            <p>4. Mempergiat, mengefektifkan dan mengoptimalkan dakwah
                amar ma'ruf nahi munkar kepada
                masyarakat, teristimewa masyarakat mahasiswa.</p>
            <p>5. Segala usaha yang tidak menyalahi asas, gerakan dan
                tujuan organisasi dengan mengindahkan
                segala hukum yang berlaku dalam negara Republik Indonesia.</p>

            <p id="bab4"><b>BAB IV</b></p>
            <p><b>ORGANISASI</b></p>

            <p>Pasal 9</p>
            <p>Keanggotaan</p>
            <p>1. Anggota IMM terdiri dari :</p>
            <p>a. Anggota Biasa, ialah mahasiswa Islam yang menyetujui
                asas dan tujuan IMM.</p>
            <p>b. Anggota Luar Biasa, ialah alumni IMM yang tetap setia
                kepada IMM dan Muhammadiyah.</p>
            <p>c. Anggota Kehormatan, ialah orang-orang yang dipandang
                berjasa mengembangkan dan
                melestarikan IMM.</p>
            <p>2. Hak dan Kewajiban serta peraturan lainnya tentang
                keanggotaan diatur dalam Anggaran Rumah
                Tangga.</p>

            <p>Pasal 10</p>
            <p>Susunan Organisasi</p>
            <p>Susunan Organisasi Ikatan Mahasiswa Muhammadiyah (IMM)
                terdiri atas ;</p>
            <p>1. Komisariat, ialah kesatuan anggota dalam suatu Kampus,
                Fakultas atau Akademi dan atau tempat
                tertentu.</p>
            <p>2. Cabang, ialah kesatuan Komisariat-Komisariat dalam suatu
                daerah kabupaten atau kota atau
                daerah tertentu.</p>
            <p>3. Daerah, ialah kesatuan Cabang-Cabang dalam suatu
                Propinsi.</p>
            <p>4. Pusat, ialah kesatuan Daerah-Daerah dalam Negara
                Republik Indonesia.</p>
            <p>5. Syarat dan Ketentuan pembentukan organisasi diatur dalam
                Anggaran Rumah Tangga.</p>

            <p id="bab5"><b>BAB V</b></p>
            <p><b>PIMPINAN</b></p>

            <p>Pasal 11</p>
            <p>Pimpinan Komisariat</p>
            <p>1. Pimpinan Komisariat adalah pimpinan tertinggi dalam
                komisariatnya yang memimpin dan
                melaksanakan kepemimpinan, peraturan-peraturan dan keputusan organisasi dalam
                lingkungannya.</p>
            <p>2. Ketua umum Pimpinan Komisariat karena jabatannya menjadi
                wakil Pimpinan Cabang di
                komisariatnya.</p>

            <p>Pasal 12</p>
            <p>Pimpinan Cabang</p>
            <p>1. Pimpinan Cabang adalah pimpinan tertinggi dalam
                Cabangnya yang memimpin dan
                melaksanakan kepemimpinan di atasnya, peraturan-peraturan dan
                keputusankeputusan
                organisasi kepada komisariat-komisariat di lingkungannya.</p>
            <p>2. Untuk mewakili kepentingan-kepentingan Cabang serta
                mengatur kerjasama antara Pimpinan
                Komisariat dalam suatu Perguruan Tinggi, Pimpinan Cabang dapat membentuk
                Koordinator
                Komisariat (KORKOM) yang ketentuan dan syarat diatur dalam peraturan
                organisasi.</p>
            <p>3. Ketua umum Pimpinan Cabang karena jabatannya menjadi
                wakil Dewan Pimpinan Daerah di
                cabangnya.</p>

            <p>Pasal 13</p>
            <p>Dewan Pimpinan Daerah</p>
            <p>1. Dewan Pimpinan Daerah adalah pimpinan tertinggi dalam
                daerahnya yang memimpin dan
                melaksanakan kepemimpinan di atasnya, peraturan-peraturan dan
                keputusankeputusan
                organisasi dalam lingkungannya.</p>
            <p>2. Ketua Umum Dewan Pimpinan Daerah karena jabatannya
                menjadi wakil Dewan Pimpinan Pusat
                di daerahnya.</p>

            <p>Pasal 14</p>
            <p>Dewan Pimpinan Pusat</p>
            <p>Dewan Pimpinan Pusat adalah pimpinan tertinggi yang
                memimpin organisasi.</p>

            <p>Pasal 15</p>
            <p>Pergantian dan Pemilihan Pimpinan</p>
            <p>1. Pergantian Pimpinan dilaksanakan pada setiap musyawarah
                tertinggi di masing-masing tingkat
                pimpinan.</p>
            <p>2. Pemilihan pimpinan diatur dalam Anggaran Rumah
                Tangga.</p>

            <p>Pasal 16</p>
            <p>Unsur Pembantu pimpinan</p>
            <p>1. Untuk melaksanakan tugas dan kewajibannya pimpinan dapat
                membentuk unsur pembantu
                pimpinan yang diserahi tugas-tugas khusus.</p>
            <p>2. Syarat dan ketentuan pembentukan Unsur Pembantu Pimpinan
                diatur dalam Anggaran Rumah
                Tangga.</p>

            <p id="bab6"><b>BAB VI</b></p>
            <p><b>MASA JABATAN</b></p>

            <p>Pasal 17</p>
            <p>1. Badan Pimpinan Harian (BPH) Pimpinan Komisariat dipilih
                untuk masa jabatan 1 (satu ) tahun.</p>
            <p>2. Badan Pimpinan Harian (BPH) Pimpinan Cabang dipilih
                untuk masa jabatan 1 ( satu) tahun.</p>
            <p>3. Badan Pimpinan Harian (BPH) Dewan Pimpinan Daerah
                dipilih untuk masa jabatan 2 ( dua) tahun.</p>
            <p>4. Badan Pimpinan Harian (BPH) Dewan Pimpinan Pusat dipilih
                untuk masa jabatan 2 ( dua) tahun.</p>

            <p id="bab7"><b>BAB VII</b></p>
            <p><b>PERMUSYAWARATAN</b></p>
            <p>Pasal 18</p>
            <p>Permusyawaratan terdiri dari :</p>
            <p>1. MUKTAMAR, ialah permusyawaratan tertinggi dalam organisasi yang diikuti oleh anggota Dewan Pimpinan Pusat, utusan-utusan Dewan Pimpinan Daerah, dan utusan-utusan
                Pimpinan Cabang.</p>
            <p>2. TANWIR, ialah permusyawaratan tertinggi dalam organisasi
                di bawah Muktamar yang diikuti oleh
                Dewan Pimpinan Pusat, dan utusan-utusan Dewan Pimpinan Daerah untuk
                membicarakan
                kepentingan-kepentingan organisasi yang tidak dapat ditangguhkan sampai
                berlangsung
                Muktamar, diadakan sekurang-kurangnya 1 (satu) kali dalam satu periode
                muktamar.</p>
            <p>3. MUSYAWARAH DAERAH, ialah permusyawaratan tertinggi dalam
                Daerah, yang diikuti oleh
                anggota Dewan Pimpinan Daerah, utusan-utusan Pimpinan Cabang, dan utusan-
                utusan Pimpinan
                Komisariat, diadakan 2 (dua) tahun sekali.</p>
            <p>4. MUSYAWARAH CABANG, ialah permusyawaratan tertinggi dalam
                Cabang yang diikuti oleh
                anggota Pimpinan Cabang, dan utusan-utusan Pimpinan Komisariat, diadakan 1
                (satu) tahun
                sekali.</p>
            <p>5. MUSYAWARAH KOMISARIAT, ialah permusyawaratan tertinggi
                dalam Komisariat yang diikuti
                oleh Pimpinan Komisariat dan seluruh anggota, diadakan 1(satu) tahun
                sekali.</p>
            <p>6. MUSYAWARAH LUAR BIASA, ialah permusyawaratan yang
                dilaksanakan apabila organisasi
                dihadapkan pada situasi kepemimpinan yang tidak mendukung untuk berlanjutnya
                kepemimpinan karena hal-hal yang mendesak dan tidak bisa ditangguhkan dengan
                disepakati dalam rapat pleno yang dihadiri oleh 3/4 oleh pimpinan
                dibawahnya.</p>

            <p>Pasal 19</p>
            <p>Keputusan</p>
            <p>1. Musyawarah dapat berlangsung dengan tidak memandang
                jumlah yang hadir, asal yang
                berkepentingan telah diundang secara sah.</p>
            <p>2. Keputusan musyawarah diusahakan dengan suara bulat.
                Apabila tidak sah dilaksanakan dengan
                lobying dan apabila tidak sah terpaksa diadakan pemungutan suara, maka
                keputusan diambil
                dengan suara terbanyak mutlak.</p>
            <p>3. Keputusan Muktamar dan Tanwir berlaku setelah disetujui
                dan disahkan oleh Pimpinan Pusat
                Muhammadiyah, dan ditanfidzkan oleh DPP IMM.</p>
            <p>4. Keputusan Musyawarah Daerah berlaku setelah disetujui
                oleh Pimpinan Wilayah Muhammadiyah, disahkan oleh Dewan Pimpinan Pusat IMM,
                dan ditanfidzkan oleh Dewan Pimpinan Daerah IMM.</p>
            <p>5. Keputusan Musyawarah Cabang berlaku setelah disetujui
                oleh Pimpinan Daerah Muhammadiyah,
                disahkan oleh Dewan Pimpinan Daerah IMM, dan ditanfidzkan oleh Pimpinan Cabang
                IMM.</p>
            <p>6. Keputusan Musyawarah Komisariat berlaku setelah disahkan
                oleh Pimpinan Cabang IMM, dan ditanfidzkan Pimpinan Komisariat IMM.</p>
            <p>7. Mekanisme pengesahan keputusan musyawarah ditetapkan
                dalam Anggaran Rumah Tangga.</p>

            <p  id="bab8"><b>BAB VIII</b></p>
            <p><b>KEUANGAN</b></p>

            <p>Pasal 20</p>
            <p>Keuangan</p>
            <p>Keuangan organisasi diperoleh dari:</p>
            <p>1. Uang Pangkal dan Iuran.</p>
            <p>2. Sumber-sumber lain yang halal dan tidak
                mengikat.</p>

            <p id="bab9"><b>BAB IX</b></p>
            <p><b>ANGGARAN RUMAH TANGGA</b></p>

            <p>Pasal 21</p>
            <p>Hal-hal yang belum diatur dalam Anggaran Dasar, akan
                diatur dalam Anggaran Rumah Tangga.
                Anggaran Rumah Tangga dapat diubah oleh Dewan Pimpinan Pusat dengan tidak
                menyalahi
                Anggaran Dasar, kemudian disahkan oleh Muktamar.</p>

            <p  id="bab10"><b>BAB X</b></p>
            <p><b>PERUBAHAN ANGGARAN DASAR</b></p>

            <p>Pasal 22</p>
            <p>Anggaran Dasar hanya dapat diubah oleh Muktamar dan
                perubahannya sah apabila diputuskan
                dengan suara sekurang-kurangnya 2/3 dari jumlah anggota Muktamar yang hadir,
                yang memang
                sengaja diundang untuk membicarakan perubahan Anggaran Dasar.</p>

            <p id="bab11"><b>BAB XI</b></p>
            <p><b>PEMBUBARAN</b></p>

            <p>Pasal 23</p>
            <p>Pembubaran IMM menjadi wewenang kedaulatan Muktamar,
                berdasarkan kesepakatan bersama.
                Setelah IMM dibubarkan segala kewajiban dan aset menjadi tanggung jawab
                Muhammadiyah.</p>

            <p id="bab12"><b>BAB XII</b></p>
            <p><b>PENUTUP</b></p>

            <p>Pasal 24</p>
            <p>Anggaran Dasar ini menjadi pengganti Anggaran Dasar
                sebelumnya, dan telah disahkan oleh
                Muktamar XVI di Surakarta, Jawa Tengah dan mulai berlaku sejak disahkannya
                oleh Pimpinan Pusat Muhammadiyah.</p>

            <p><i>Ditetapkan di : Solo - Jawa Tengah</i></p>
