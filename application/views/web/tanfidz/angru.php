            <p>KEANGGOTAAN</p>
            <p>Pasal 3</p>
            <p>Anggota Biasa</p>
            <p>1. Yang dapat diterima menjadi anggota biasa adalah:</p>
            <p>a. Mahasiswa yang sedang menempuh perkuliahan di perguruan tinggi atau yang setingkat.</p>
            <p>b. Mahasiswa yang telah menyelesaikan perkuliahan sebagaimana dimaksud dalam pasal 3 ayat 1 huruf (a) paling lambat 2 (dua) tahun setelah yudisium atau maksimal usia 30 tahun.</p>
            <p>2. Prosedur menjadi anggota biasa :</p>
            <p>a. Calon anggota harus mengikuti dan dinyatakan lulus perkaderan Darul Arqam Dasar (DAD).</p>
            <p>b. Permintaan menjadi anggota biasa diajukan secara tertulis oleh Pimpinan Komisariat kepada Dewan Pimpinan Daerah melalui Pimpinan Cabang.</p>
            <p>c. Apabila permintaan menjadi anggota diterima, kepadanya diberikan Kartu Tanda Anggota oleh Dewan Pimpinan Daerah atas nama DPP IMM.</p>
            <p>d. Bentuk tanda anggota ditentukan oleh Dewan Pimpinan Pusat.</p>
            <p>e. Setiap 6 (enam) bulan sekali DPD melaporkan database keanggotaan kepada Dewan Pimpinan Pusat IMM.</p>
            <p>f. Bagi Calon Anggota yang berasal dari Organisasi Otonom Muhammadiyah, syarat keanggotaan diatur oleh Peraturan Khusus yang dibuat oleh DPP.</p>
            <p>g. Anggota IMM tidak boleh merangkap pada organisasi ekstra kampus yang sejenis.</p>
            <p>Pasal 4</p>
            <p>Anggota Luar Biasa</p>
            <p>1. Anggota luar biasa adalah alumni IMM yang telah memenuhi kriteria seperti anggota biasasebagaimana pasal 3 dan mendukung gerakan dakwah Muhammadiyah.</p>
            <p>2. Anggota luar biasa atas usulan pimpinan cabang IMM dan ditetapkan oleh DPD IMM.</p>
            <p>Pasal 5</p>
            <p>Anggota Kehormatan</p>
            <p>1. Anggota kehormatan adalah orang yang berasal dari luar kalangan IMM yang telah memberikan kontribusi luar biasa pada ikatan.</p>
            <p>2. Anggota kehormatan dapat diusulkan oleh pimpinan IMM pada tingkat dimana yang bersangkutan berada setelah dipertimbangkan dan ditetapkan DPP IMM.</p>
            <p>Pasal 6</p>
            <p>Hak dan Kewajiban</p>
            <p>1. Anggota biasa berhak menyatakan pendapat, suara, memilih dan dipilih.</p>
            <p>2. Kewajiban anggota biasa adalah:</p>
            <p>a. Mempelajari dan mengamalkan kepribadian dan khittah perjuangan Muhammadiyah.</p>
            <p>b. Menjadi tauladan utama bagi mahasiswa.</p>
            <p>c. Tunduk dan taat kepada keputusan organisasi, peraturan-peraturan dan menjaga nama baik IMM.</p>
            <p>d. Turut melaksanakan dan mendukung usaha-usaha organisasi.</p>
            <p>e. Membayar uang pangkal dan iuran yang besarnya ditetapkan oleh Dewan Pimpinan Pusat IMM.</p>
            <p>Pasal 7</p>
            <p>Pemberhentian Anggota</p>
            <p>Keanggotaan berhenti karena:</p>
            <p>1. Meninggal dunia.</p>
            <p>2. Permintaan sendiri.</p>
            <p>3. Keputusan Dewan Pimpinan Daerah atas usulan Pimpinan Cabang karena pelanggaran terhadap aturan dan ketentuan organisasi.</p>
            <p>4. Keputusan Dewan Pimpinan Daerah tentang pemberhentian anggota sesuai pasal 7 ayat 3 hanya dapat dilaksanakan setelah :</p>
            <p>a. Diadakan penelitian oleh Pimpinan Cabang;</p>
            <p>b. Diberikan peringatan oleh Pimpinan Cabang secara tertulis;</p>
            <p>c. Dilakukan skorsing oleh Pimpinan Cabang, apabila peringatan tersebut pada pasal 7 ayat 4 huruf (b) tidak diindahkan;</p>
            <p>d. Anggota yang diberhentikan oleh Dewan Pimpinan Daerah diberi kesempatan membela diri dalam musyawarah yang diadakan oleh Dewan Pimpinan Daerah IMM.</p>
            <br>
            <p id="bab3"><b>BAB III</b></p>
            <p>SUSUNAN ORGANISASI</p>
            <p>Pasal 8</p>
            <p>Komisariat</p>
            <p>1. Pembentukan dan pengesahan serta ketentuan luas teritorial komisariat ditetapkan dengan surat keputusan Dewan Pimpinan Daerah atas usul Pimpinan Cabang yang bersangkutan.</p>
            <p>2. Komisariat berkewajiban melaksanakan usaha-usaha organisasi untuk menghimpun, membina dan meningkatkan kualitas serta menyalurkan bakat dan minat anggotanya untuk kepentingan organisasi, minimal melaksanakan kegiatan perkaderan.</p>
            <p>Pasal 9</p>
            <p>Cabang</p>
            <p>1. Cabang dibentuk oleh Dewan Pimpinan Pusat, terdiri dari sekurang kurangnya 3 (tiga) komisariat yang telah disahkan.</p>
            <p>2. Pembentukan dan pengesahan serta ketentuan luas teritorial cabang ditetapkan dengan surat keputusan Dewan Pimpinan Pusat atas usul Dewan Pimpinan Daerah yang bersangkutan.</p>
            <p>3. Pimpinan Cabang dapat membentuk Koordinator Komisariat (KORKOM) dengan mengadakan rapat pleno yang dihadiri oleh sekurang-kurangnya 2/3 (dua pertiga) unsur pimpinan cabang dan 2 (dua) orang perwakilan pimpinan komisariat.</p>
            <p>Pasal 10</p>
            <p>Daerah</p>
            <p>1. Daerah dapat dibentuk oleh Dewan Pimpinan Pusat terdiri dari sekurang kurangnya 3 ( tiga) cabang yang telah disahkan.</p>
            <p>2. Pembentukan dan pengesahan serta ketentuan luas teritorial daerah ditetapkan dengan surat keputusan Dewan Pimpinan Pusat atas usul Musyawarah Daerah dan setelah mendengar pertimbangan calon Dewan Pimpinan Daerah yang bersangkutan.</p>
