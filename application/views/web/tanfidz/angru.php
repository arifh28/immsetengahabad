            <p>4. Melanggar konstitusi ikatan dan persyarikatan.</p>
            <p>Pasal 13</p>
            <p>Dewan Pimpinan Pusat</p>
            <p>1. Dewan Pimpinan Pusat disusun oleh formatur yang dipilih oleh Muktamar.</p>
            <p>2. Dewan Pimpinan Pusat memimpin organisasi, mentanfidzkan keputusan serta mengawasi pelaksanaannya.</p>
            <p>3. Untuk melaksanakan pekerjaan sehari-hari yang bersifat administratif, Dewan Pimpinan Pusat mengangkat Sekretaris Eksekutif.</p>
            <p>4. Untuk melaksanakan tugas dan kewajibannya Dewan Pimpinan Pusat dapat membentuk Unsur Pembantu Pimpinan yang diserahi tugas dan menyelenggarakan pekerjaan khusus.</p>
            <p>5. Struktur Dewan Pimpinan Pusat terdiri dari 1 (satu) orang Ketua Umum, 1 (satu) orang Sekretaris Jenderal, 1 (satu) orang Bendahara Umum, 11 (Sebelas) orang Ketua bidang, 11 (sebelas) orang sekretaris bidang dan 3 (tiga) orang bendahara.</p>
            <p>Pasal 14</p>
            <p>Dewan Pimpinan Daerah</p>
            <p>1. Dewan Pimpinan Daerah disusun oleh formatur yang dipilih oleh Musyawarah Daerah dan disahkan oleh Dewan Pimpinan Pusat.</p>
            <p>2. Dewan Pimpinan Daerah adalah wakil Dewan Pimpinan Pusat di daerahnya.</p>
            <p>3. Dewan Pimpinan Daerah dapat membentuk Unsur Pembantu Pimpinan sesuai dengan ketentuan dan peraturan yang berlaku.</p>
            <p>4. Dewan Pimpinan Daerah harus memberikan laporan kepada Dewan Pimpinan Pusat sekurangkurangnya 6 (enam) bulan sekali dan atau apabila ada hal-hal yang dianggap perlu.</p>
            <p>5. Struktur Dewan Pimpinan Daerah terdiri dari 1 (satu) orang ketua umum, 1 (satu) orang Sekretaris umum, 1 (satu) orang bendahara umum, 10 (sepuluh) orang ketua bidang, 10 (sepuluh) orang sekretaris bidang dan 3 (tiga) orang wakil bendahara.</p>
            <p>6. Dalam keadaan tertentu Struktur Dewan Pimpinan Daerah sekurang-kurangnya terdiri dari 13 (tiga belas) orang Badan Pimpinan Harian.</p>
            <p>Pasal 15</p>
            <p>Pimpinan Cabang</p>
            <p>1. Pimpinan Cabang disusun oleh formatur yang dipilih oleh Musyawarah Cabang dan disahkan oleh Dewan Pimpinan Daerah.</p>
            <p>2. Pimpinan Cabang adalah wakil Dewan Pimpinan Daerah di daerahnya.</p>
            <p>3. Pimpinan Cabang memberikan laporan kepada Dewan Pimpinan Daerah sekurang-kurangnya 6 (enam) bulan dan atau apabila ada hal-hal yang dianggap perlu.</p>
            <p>4. Struktur Pimpinan Cabang terdiri dari 1 (satu) orang ketua umum, 1 (satu) orang sekretaris umum, 1 (satu) orang bendahara umum, 10 (sepuluh) orang ketua bidang, 10 (sepuluh) orang sekretaris bidang dan 3 (tiga) orang wakil bendahara.</p>
            <p>5. Dalam keadaan tertentu struktur Pimpinan Cabang sekurang-kurangnya terdiri dari 13 ( tiga belas) orang Badan Pimpinan Harian.</p>
            <p>Pasal 16</p>
            <p>Pimpinan Komisariat</p>
            <p>1. Pimpinan Komisariat disusun oleh formatur yang dipilih oleh Musyawarah Komisariat dan disahkan oleh Pimpinan Cabang.</p>
            <p>2. Pimpinan Komisariat memberikan laporan kepada Pimpinan Komisariat sekurangkurangnya 3 (tiga) bulan sekali dan atau apabila ada hal hal yang dipandang perlu.</p>
            <p>3. Struktur Pimpinan Komisariat terdiri dari 1 (satu) orang ketua umum, 1 (satu) orang sekretaris umum, 1 (satu) orang bendahara umum, 10 (sepuluh) orang ketua bidang, 10 (sepuluh) orang sekretaris bidang dan 3 (tiga) orang wakil bendahara.</p>
            <p>4. Dalam keadaan tertentu struktur Pimpinan Komisariat sekurang-kurangnya terdiri dari 13 (tigabelas) orang Badan Pimpinan Harian.
            <p>Pasal 17</p>
            <p>Unsur Pembantu Pimpinan</p>
            <p>1. Unsur Pembantu Pimpinan terdiri dari Lembaga Semi Otonom (LSO) dan Lembaga Otonom (LO).</p>
            <p>2. Lembaga Semi Otonom adalah Unsur Pembantu Pimpinan yang menjalankan sebagian tugas pokok IMM.</p>
            <p>3. Lembaga Otonom adalah Unsur Pembantu Pimpinan yang menjalankan tugas pendukung IMM.</p>
            <p>4. Unsur Pembantu Pimpinan dibentuk dan sahkan oleh pimpinan yang bersangkutan.</p>
            <p>5. Ketentuan tentang pembentukan dan tugas Unsur Pembantu Pimpinan diatur dalam Peraturan Organisasi.</p>
            <p>Pasal 18</p>
            <p>Pemilihan Pimpinan</p>
            <p>1. Pemilihan dilakukan secara langsung, bebas, rahasia, jujur dan adil.</p>
            <p>2. Pemilihan Pimpinan dilakukan dengan mekanisme pemilihan Formatur.</p>
            <p>3. Pelaksanaan pemilihan pimpinan dilakukan oleh Panitia Pemilihan yang dibentuk dan ditetapkan oleh pimpinan masing-masing tingkatan bersama pimpinan dibawahnya melalui rapat pleno untuk satu kali pemilihan.</p>
            <p>4. Pelaksanaan pemilihan pimpinan diatur berdasarkan tata tertib pemilihan pimpinan yang ditetapkan oleh Tanwir dan telah ditanfidzkan oleh Dewan Pimpinan Pusat.</p>
            <p>Pasal 19</p>
            <p>Pergantian dan Perubahan Pimpinan</p>
            <p>1. Pimpinan IMM yang telah habis masa jabatannya tetap menjalankan tugasnya sampai dilakukan serah terima jabatan dengan pimpinan yang baru.</p>
            <p>2. Dalam satu masa jabatan, dapat dilakukan perubahan pimpinan.</p>
            <p>3. Perubahan pimpinan diatur dalam peraturan khusus yang ditetapkan oleh Dewan Pimpinan Pusat.</p>
            <p>4. Setiap pergantian dan perubahan pimpinan IMM harus menjamin adanya peningkatan efisiensi dan efektifitas jalannya kepemimpinan.</p>
            <p>5. Setiap pergantian dan perubahan pimpinan IMM harus memenuhi syarat � syarat seusai dengan ketentuan.</p>
